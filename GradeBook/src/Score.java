
public class Score {
	int score = 0;
	double percent = 0.0;
	int assignmentNumber = 0;
	int studentId = 0;
	int courseId = 0;
	
	public int getScore()
	{
		return score;
	}
	public double getPercent()
	{
		return percent;
	}
	public int getAssignment()
	{
		return assignmentNumber;
	}
	public int getStudent()
	{
		return studentId;
	}
	public int getCourse()
	{
		return courseId;
	}
}
